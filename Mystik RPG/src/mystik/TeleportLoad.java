package mystik;

public class TeleportLoad {
	    public int fromX;
	    public int fromY;
	    public int fromMap;
	    public int toX;
	    public int toY;
	    public int toMap;
	    public int item_req;

	    public int getFromX() { return fromX; }
	    public int getFromY() { return fromY; }
	    public int getFromMap() { return fromMap; }
	    public int getToX() { return toX; }
	    public int getToY() { return toY; }
	    public int getToMap() { return toMap; }
	    public int getItemReq() { return item_req; }

	    public void setX(int fromX) { this.fromX = fromX; }
	    public void setY(int fromY) { this.fromY = fromY; }
	    public void setFromMap(int fromMap) { this.fromMap = fromMap; }
	    public void setToX(int toX) { this.toX = toX; }
	    public void setToY(int toY) { this.toY = toY; }
	    public void setToMap(int toMap) { this.toMap = toMap; }
	    public void setItemReq(int item_req) { this.item_req = item_req; }
	}
