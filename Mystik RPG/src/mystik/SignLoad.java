package mystik;

public class SignLoad {
	    public int x;
	    public int y;
	    public String text;

        public int getSignX() { return y; }
        public int getSignY() { return x; }
        public String getSignText() { return text; }

        public void setSignX(int x) { this.x = x; }
        public void setSignY(int y) { this.y = y; }
        public void setSignText(String text) { this.text = text; }
	}